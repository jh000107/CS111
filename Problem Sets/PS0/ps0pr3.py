# Computes the integers 0 through 4 using exactly 4 fours.
#
# Complete the rest of the program below.

zero = 4 * 4 // 4 - 4
print('zero =', zero)

one = 4 * 4 // 4 // 4
print('one =', one)

two = 4 // 4 + 4 // 4
print('two =', two)

three = (4 + 4 + 4) // 4
print('three =', three)

four = (4 - 4) * 4 + 4
print('four =', four)
